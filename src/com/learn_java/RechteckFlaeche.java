package com.learn_java;

public class RechteckFlaeche {
    public static void main(String[] args) {
        double breite, hoehe, flaeche;
        breite = 5.5;
        hoehe = 3.5;
        flaeche = breite * hoehe;

        System.out.println("Berechng der Fläche des Rechtecks mit der");
        System.out.println("Breite " + breite + " und der Höhe " + hoehe);
        System.out.println("Flaeche " + flaeche);
    }
}